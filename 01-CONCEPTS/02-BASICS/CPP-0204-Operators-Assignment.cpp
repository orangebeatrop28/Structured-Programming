#include <iostream>
using namespace std;

int main() {

    // Assignment
    // =  += -= *= /= %=
    int base = 10;
    cout << base << endl;

    // base += 2;
    base = base + 2;
    cout << base << endl;

    base -= 8;
    cout << base << endl;

    base *= 3;
    cout << base << endl;

    base /= 4;
    cout << base << endl;

    base %= 2;
    cout << base << endl;
    return 0;
}
